const stockRotation = ["in stock", "out of stock", "preorder", "unknown"] as const;

function createOffers(): Offer[] {
  return productSeeds.flatMap((product, productIndex) =>
    retailerSeeds.map((retailer, retailerIndex) => {
      const stock =
        retailerIndex === 0 ? product.stock : stockRotation[(productIndex + retailerIndex) % 4];
      let imageSource: Offer["imageSource"] = "retailer";
      if (retailerIndex % 4 === 2) imageSource = "official";
      if (product.slug === "life-ring" && retailerIndex === 5) imageSource = "none";
      const presence = product.slug === "mirielle" && retailerIndex === 2 ? "inactive" : "active";
      const match = product.match;

      return {
        id: `${product.slug}-${retailer.storeId}`,
        title: product.title,
        canonicalTitle: product.canonicalTitle,
        retailer: retailer.retailer,
        siteUrl: retailer.siteUrl,
        storeId: retailer.storeId,
        listingUrl: `https://example.com/${retailer.storeId}/${product.slug}`,
        stock,
        checkedMinutes: 2 + ((productIndex * 11 + retailerIndex * 7) % 94),
        price: product.price,
        match,
        coverage: retailer.coverage,
        presence,
        imageSource,
        palette: product.palette,
      };
    }),
  );
}

